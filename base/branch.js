const color = 'blue';
switch(color){
    case 'blue':
        console.log('color is blue.');
        break;
    case 'red':
        console.log('color is red.');
        break;
    case 'green':
        console.log('color is green.');
        break;
    default:
        console.log('color is NOT blue, red or green.');
        break;
}